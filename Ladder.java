public class Ladder {
	public static void main ( String[] args) {
		for ( int i=0;i<=5;i++) {
			if ( i == 1 )
				System.out.println("i is One");
			else if ( i == 2)
				System.out.println("i is Two");
			else if ( i == 3)
				System.out.println("i is Three");
			else if ( i == 4)
				System.out.println("i is Four");
			else
				System.out.println("i is not between One and Four");
		}
	}
}
